    private void init(){
        panelUI1.setLayout(new FlowLayout(FlowLayout.CENTER, 20, 100));
        System.out.println(parqueaderos);
        for (JsonElement parqueadero : parqueaderos) {
            JButton btn = new JButton(parqueadero.getAsJsonObject().get("NombreParqueadero").getAsString());
            btn.setPreferredSize(new Dimension(200, 50)); // Modifica las dimensiones seg√∫n tus necesidades
            panelUI1.add(btn);

            btn.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    AplictionVen aplictionVen = new AplictionVen(parqueadero.getAsJsonObject().get("NombreVendedor").getAsString(), parqueadero.getAsJsonObject().get("idpark").getAsString(), parqueadero.getAsJsonObject().get("NombreParqueadero").getAsString(), parqueadero.getAsJsonObject().get("idusuario").getAsString());
                    aplictionVen.setVisible(true);
                    dispose();
                }
            });
        }
        panelUI1.revalidate();
        panelUI1.repaint();
    }

